#include <iostream>
using namespace std;

int main()
{
    float bil1, bil2;

    cout << "Bilangan float pertama: ";
    cin >> bil1;
    cout << "Bilangan float kedua: ";
    cin >> bil2;

    cout << "Hasil Penjumlahan: " << bil1 + bil2 << endl;
    cout << "Hasil Pengurangan: " << bil1 - bil2 << endl;
    cout << "Hasil Perkalian: " << bil1 * bil2 << endl;
    cout << "Hasil Pembagian: " << bil1 / bil2 << endl;

    return 0;
}
